    private void sw4(int cr, char Sym){
        switch(cr){
                case 43:
                    result=Double.parseDouble(txt_SaveResult.getText().substring(0, txt_SaveResult.getText().length()-1))+Double.parseDouble(btngetData);
                    Symbol = Sym;
                    ChackIf(result);
                    break;
                case 42:
                    if(txt_SaveResult.getText().substring(txt_SaveResult.getText().length()-1, txt_SaveResult.getText().length())=="="){
                        txt_SaveResult.setText(btngetData+Sym);
                        break;
                    }
                    result=Double.parseDouble(txt_SaveResult.getText().substring(0, txt_SaveResult.getText().length()-1))*Double.parseDouble(btngetData);
                    Symbol = Sym;
                    ChackIf(result);
                    break;
                case 45:
                    if(txt_SaveResult.getText().substring(txt_SaveResult.getText().length()-1, txt_SaveResult.getText().length())=="="){
                        txt_SaveResult.setText(btngetData+Sym);
                        break;
                    }
                    result=Double.parseDouble(txt_SaveResult.getText().substring(0, txt_SaveResult.getText().length()-1))-Double.parseDouble(btngetData);
                    Symbol = Sym;
                    ChackIf(result);
                    break;
                case 47:
                    if(txt_SaveResult.getText().substring(txt_SaveResult.getText().length()-1, txt_SaveResult.getText().length())=="="){
                        txt_SaveResult.setText(btngetData+Sym);
                        break;
                    }
                    result=Double.parseDouble(txt_SaveResult.getText().substring(0, txt_SaveResult.getText().length()-1))/Double.parseDouble(btngetData);
                    Symbol = Sym;
                    ChackIf(result);
                    break;
                default:
                    break;
            }
    }
    private double onlysw(int cr){
        int idx;
        switch(cr){
                case 43:
                    idx = txt_SaveResult.getText().indexOf('+');
                    result=Double.parseDouble(txt_SaveResult.getText().substring(0, idx))+Double.parseDouble(btngetData);
                    return result;
                case 42:
                    idx = txt_SaveResult.getText().indexOf('*');
                    result=Double.parseDouble(txt_SaveResult.getText().substring(0, idx))*Double.parseDouble(btngetData);
                    return result;
                case 45:
                    idx = txt_SaveResult.getText().indexOf('-');
                    result=Double.parseDouble(txt_SaveResult.getText().substring(0, idx))-Double.parseDouble(btngetData);
                    return result;
                case 47:
                    idx = txt_SaveResult.getText().indexOf('/');
                    result=Double.parseDouble(txt_SaveResult.getText().substring(0, idx))/Double.parseDouble(btngetData);
                    return result;
                default:
                    return result;
            }
    }
    
    private void btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddActionPerformed
        // TODO add your handling code here:
        btngetData = txt_Result.getText();
        if(Symbol == ' '){
            Symbol = btn_Add.getText().charAt(0);            
            btngetData += Symbol;
            txt_SaveResult.setText(btngetData);
            txt_Result.setText("0");
        }else if(Symbol == '+'){
            result=Double.parseDouble(txt_SaveResult.getText().substring(0, txt_SaveResult.getText().length()-1))+Double.parseDouble(btngetData);
            ChackIf(result);
        }else{
            ascSym = (int)Symbol;
            sw4(ascSym, btn_Add.getText().charAt(0));
        }
    }//GEN-LAST:event_btn_AddActionPerformed

