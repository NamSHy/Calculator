    private double equalsw(int cr){
        int idx;
        int idx2;
        switch(cr){
                case 43:
                    idx = txt_SaveResult.getText().indexOf('+');
                    idx2 = txt_SaveResult.getText().indexOf('=');
                    result=Double.parseDouble(btngetData)+Double.parseDouble(txt_SaveResult.getText().substring(idx+1, idx2));
                    return result;
                case 42:
                    idx = txt_SaveResult.getText().indexOf('*');
                    idx2 = txt_SaveResult.getText().indexOf('=');
                    result=Double.parseDouble(btngetData)*Double.parseDouble(txt_SaveResult.getText().substring(idx+1, idx2));
                    return result;
                case 45:
                    idx = txt_SaveResult.getText().indexOf('-');
                    idx2 = txt_SaveResult.getText().indexOf('=');
                    result=Double.parseDouble(btngetData)-Double.parseDouble(txt_SaveResult.getText().substring(idx+1, idx2));
                    return result;
                case 47:
                    idx = txt_SaveResult.getText().indexOf('/');
                    idx2 = txt_SaveResult.getText().indexOf('=');
                    result=Double.parseDouble(btngetData)/Double.parseDouble(txt_SaveResult.getText().substring(idx+1, idx2));
                    return result;
                default:
                    return result;
            }
    }
    private int idxsw(int ascSym){
        int idx;
        switch(ascSym){
                case 43:
                    idx = txt_SaveResult.getText().indexOf('+');
                    return idx;
                case 42:
                    idx = txt_SaveResult.getText().indexOf('*');
                    return idx;
                case 45:
                    idx = txt_SaveResult.getText().indexOf('-');
                    return idx;
                case 47:
                    idx = txt_SaveResult.getText().indexOf('/');
                    return idx;
                default:
                    return 0;
            }
    }
    
